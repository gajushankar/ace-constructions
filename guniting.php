				<p>
				The guniting is the most effective process of repairing concrete work which has been damaged due to inferior work or other reasons. It is also used for providing an impervious layer. </p>
				<p>
				The gunite is a mixture of cement and sand, the usual proportion being 1:3. A cement gun is used to deposit this mixture on the concrete surface under a pressure of about 20 to 30 N/cm2.</p>
				

				<h4>Procedure</h4>
				<p>
				The cement is mixed with slightly moist sand and then necessary water is added as the mixture comes out from the cement gun. A regulating valve is provided to regulate the quantity of water.
				</p>
				<p>
				The surface to be treated is cleaned and washed. The nozzle of gun is generally kept at a distance of about 750 mm to 850 mm from the surface to be treated and the velocity of nozzle varies from 120 to 160 m/sec.
				</p>

				<h4>Advantages</h4>
				<p>
				<ol>	
					The high compressive strength is obtained. Strength of about 56 to 70 n/mm2 at 28 days is generally obtained.
				</ol>
				<ol>The high impermeability is achieved.</ol>
				<ol>The repairs are carried out in any situation in a short time.</ol>
				</p>

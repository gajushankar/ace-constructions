				<p>
				Waterproofing is the process of making an object or structure waterproof or water-resistant, so that it remains relatively unaffected by water or resisting the ingress of water under specified conditions. Such items may be used in wet environments or under water to specified depths.</p>
				<p>
				Water resistant and waterproof often refer to penetration of water in its liquid state and possibly under pressure, whereas damp proof refers to resistance to humidity or dampness. Permeation of water vapor through a material or structure is reported as a moisture vapor transmission rate.</p>
				<p>
				The hulls of boats and ships were once waterproofed by applying tar or pitch. Modern items may be waterproofed by applying water-repellent coatings or by sealing seams with gaskets or o-rings. Waterproofing is used in reference to building structures (such as basements, decks, or wet areas), watercraft, canvas, clothing (raincoats or waders), electronic devices and paper packaging (such as cartons for liquids).</p>

				<p>In building construction, waterproofing is a fundamental aspect of creating a building envelope, which is a controlled environment. The roof covering materials, siding, foundations, and all of the various penetrations through these surfaces must be water-resistant and sometimes waterproof. Roofing materials are generally designed to be water-resistant and shed water from a sloping roof, but in some conditions, such as ice damming and on flat roofs, the roofing must be waterproof. [3] Many types of waterproof membrane systems are available, including felt paper or tar paper with asphalt or tar to make a built-up roof, other bituminous waterproofing, ethylene propylene diene monomer EPDM rubber, hypalon, polyvinyl chloride, liquid roofing, and more.

Walls are not subjected to standing water, and the water-resistant membranes used as housewraps are designed to be porous enough to let moisture escape. Walls also have vapor barriers or air barriers. Damp proofing is another aspect of waterproofing. Masonry walls are built with a damp-proof course to prevent rising damp, and the concrete in foundations needs to be damp-proofed or waterproofed with a liquid coating, basement waterproofing membrane (even under the concrete slab floor where polyethylene sheeting is commonly used), or an additive to the concrete.</p>
